# 流程控制


# 流程就是计算机执行的顺序，顺序结构，分支结构，循环结构

# 分支结构 ：  单向分支  双向分支  多向分支 。。。

# 单向分支：如果 判断 表达式的结果 成立，那么就要执行 代码区间，如果表达式不成立，则不执行代码区间
# 程序的女友给程序员打电话说：晚上下班回来的路上买十个包子，如果有看到卖西瓜的就买一个
