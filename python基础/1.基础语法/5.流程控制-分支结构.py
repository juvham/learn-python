# 判断是否有卖西瓜的
if mxg:
    baozi = 1

# print(baozi)


# 双向分支

person = '男'
# 双向分支： 表达式成立则执行真区间，如果不成立 则执行 假区间，
if person == '女':
    # 真区间
    print('上前搭讪，美女你好漂亮。。。')
else:
    # 假区间
    print('上前一步走，哥们借个火。。。')

# 多向分支：就是需要判断多个表达式的结果，会执行其中符合条件的一个

score = 36
if score >= 90 and score <= 100:
    print('给你个大大的奖励。。买个手机')
elif score >= 80 and score < 90:
    print('今晚吃顿好的，庆祝一下。。。')
elif score >= 70 and score < 80:
    print('下次努力，加油吧 小伙子。。。')
elif score >= 60 and score < 70:
    print('抓紧复习吧，下次看你能考多少。。。')
else:
    print('给你个大嘴巴子。。。')



# 巢状分支： 在分支条件中，嵌套分支
'''
if 表达式a:
    if 表达式b：
        if 表达式c：
            。。。。
'''

sex = '男'
age = 25
